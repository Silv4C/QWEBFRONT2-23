//tipo: Boolean - True ou false
const condicional = true;

//tipo: Null - Valor Vazio
const vazio = null;

//tipo: Number - Qualquer tipo de número inteiro ou decimal
const idade = 25;

//tipo: String - Textos delimitados entre aspas
const nome = "Fernando";

//tipo: Undefined - Variável declarada mas sem nenhum valor atribuído
let escola;

console.log(typeof condicional);
console.log(typeof vazio);
console.log(typeof idade);
console.log(typeof nome);
console.log(typeof escola);