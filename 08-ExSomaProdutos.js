//Declaração da variável
let produto1, produto2, produto3;

//Atribuição da variável
//Entrada de dados
produto1 = 9.99;
produto2 = 13.25;
produto3 = 22.30;

//Processamento
let valorTotal = produto1 + produto2 + produto3

//Saída
//Template String. 
console.log(`O primeiro produto é: ${produto1}`)
console.log(`O segundo produto é: ${produto2}`)
console.log(`O terceiro produto é: ${produto3}`)
console.log(`Soma dos produtos é: ${valorTotal.toFixed(2)}`)