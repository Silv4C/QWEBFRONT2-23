//Operadores Matemáticos
/*
    + [SOMA]
    - [SUBTRAÇÃO]
    * [MULTIPLICAÇÃO]
    / [DIVISÃO]
    ++ [INCREMENTO]
    -- [DECREMENTO]
    ** [EXPONENCIAÇÃO]
    % [MÓDULO]

*/

let valor1 = 50;
let valor2 = 5;

let expo = valor1 ** valor2;
console.log("Resultado da exponenciação: " + expo);

let sub = valor1 - valor2;
console.log("Resultado da subtração: " + sub);

let soma = valor1 + valor2;
console.log("Resultado da soma: " + soma);

let mult = valor1 * valor2;
console.log("Resultado da multiplicação: " + mult);

let div = valor1 / valor2;
console.log("Resultado da divisão: " + div);

let inc = ++valor1;
console.log("Resultado do incremento: " + inc);

let dec = --valor2;
console.log("Resultado do decremento: " + dec);

let mod = valor1 % valor2;
console.log("Resultado do resto: " + mod);